#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π —Å–µ—Ä–≤–∏—Å–∞ –æ–±—ä—è–≤–ª–µ–Ω–∏–π
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./setup_auto_update.sh [daily|weekly|manual]

set -e

UPDATE_TYPE=${1:-daily}
PROJECT_DIR="/var/www/advertisements"
UPDATE_SCRIPT="$PROJECT_DIR/update.sh"

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

log() {
    echo -e "${GREEN}[$(date '+%Y-%m-%d %H:%M:%S')]${NC} $1"
}

warn() {
    echo -e "${YELLOW}[$(date '+%Y-%m-%d %H:%M:%S')] WARNING:${NC} $1"
}

error() {
    echo -e "${RED}[$(date '+%Y-%m-%d %H:%M:%S')] ERROR:${NC} $1"
    exit 1
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è —Å–∫—Ä–∏–ø—Ç–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
if [ ! -f "$UPDATE_SCRIPT" ]; then
    error "–°–∫—Ä–∏–ø—Ç –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω: $UPDATE_SCRIPT"
fi

# –î–µ–ª–∞–µ–º —Å–∫—Ä–∏–ø—Ç –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º
chmod +x "$UPDATE_SCRIPT"

log "üîß –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π..."

case $UPDATE_TYPE in
    daily)
        log "üìÖ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –µ–∂–µ–¥–Ω–µ–≤–Ω—ã—Ö –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π –≤ 3:00 —É—Ç—Ä–∞..."
        
        # –°–æ–∑–¥–∞–Ω–∏–µ cron –∑–∞–¥–∞—á–∏ –¥–ª—è –µ–∂–µ–¥–Ω–µ–≤–Ω–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
        (crontab -l 2>/dev/null; echo "0 3 * * * cd $PROJECT_DIR && $UPDATE_SCRIPT --backup --migrate >> /var/log/django/auto_update.log 2>&1") | crontab -
        
        log "‚úÖ –ï–∂–µ–¥–Ω–µ–≤–Ω—ã–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã –Ω–∞ 3:00 —É—Ç—Ä–∞"
        log "üìù –õ–æ–≥–∏ –±—É–¥—É—Ç —Å–æ—Ö—Ä–∞–Ω—è—Ç—å—Å—è –≤ /var/log/django/auto_update.log"
        ;;
        
    weekly)
        log "üìÖ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –µ–∂–µ–Ω–µ–¥–µ–ª—å–Ω—ã—Ö –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π –ø–æ –≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å—è–º –≤ 2:00 —É—Ç—Ä–∞..."
        
        # –°–æ–∑–¥–∞–Ω–∏–µ cron –∑–∞–¥–∞—á–∏ –¥–ª—è –µ–∂–µ–Ω–µ–¥–µ–ª—å–Ω–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
        (crontab -l 2>/dev/null; echo "0 2 * * 0 cd $PROJECT_DIR && $UPDATE_SCRIPT --backup --migrate >> /var/log/django/auto_update.log 2>&1") | crontab -
        
        log "‚úÖ –ï–∂–µ–Ω–µ–¥–µ–ª—å–Ω—ã–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã –Ω–∞ –≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ –≤ 2:00 —É—Ç—Ä–∞"
        log "üìù –õ–æ–≥–∏ –±—É–¥—É—Ç —Å–æ—Ö—Ä–∞–Ω—è—Ç—å—Å—è –≤ /var/log/django/auto_update.log"
        ;;
        
    manual)
        log "üîß –ù–∞—Å—Ç—Ä–æ–π–∫–∞ —Ä—É—á–Ω–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è..."
        
        # –°–æ–∑–¥–∞–Ω–∏–µ –∞–ª–∏–∞—Å–∞ –¥–ª—è —É–¥–æ–±–Ω–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
        echo "alias update-ads='cd $PROJECT_DIR && $UPDATE_SCRIPT --backup --migrate'" >> ~/.bashrc
        echo "alias update-ads-quick='cd $PROJECT_DIR && $UPDATE_SCRIPT'" >> ~/.bashrc
        echo "alias update-ads-safe='cd $PROJECT_DIR && $UPDATE_SCRIPT --backup --migrate --no-restart'" >> ~/.bashrc
        
        log "‚úÖ –°–æ–∑–¥–∞–Ω—ã –∞–ª–∏–∞—Å—ã –¥–ª—è —Ä—É—á–Ω–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è:"
        log "   - update-ads: –ø–æ–ª–Ω–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å –±—ç–∫–∞–ø–æ–º –∏ –º–∏–≥—Ä–∞—Ü–∏—è–º–∏"
        log "   - update-ads-quick: –±—ã—Å—Ç—Ä–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –±–µ–∑ –±—ç–∫–∞–ø–∞"
        log "   - update-ads-safe: –±–µ–∑–æ–ø–∞—Å–Ω–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –±–µ–∑ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞"
        ;;
        
    *)
        error "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ç–∏–ø –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è: $UPDATE_TYPE"
        echo "–î–æ—Å—Ç—É–ø–Ω—ã–µ —Ç–∏–ø—ã: daily, weekly, manual"
        exit 1
        ;;
esac

# –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è –ª–æ–≥–æ–≤
sudo mkdir -p /var/log/django
sudo chown www-data:www-data /var/log/django

# –°–æ–∑–¥–∞–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–∞ –¥–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π
cat > "$PROJECT_DIR/monitor_updates.sh" << 'EOF'
#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π
LOG_FILE="/var/log/django/auto_update.log"
EMAIL_RECIPIENT="admin@your-domain.com"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
if [ -f "$LOG_FILE" ]; then
    LAST_UPDATE=$(tail -n 50 "$LOG_FILE" | grep "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ" | tail -n 1)
    if [ -n "$LAST_UPDATE" ]; then
        echo "‚úÖ –ü–æ—Å–ª–µ–¥–Ω–µ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ: $LAST_UPDATE"
    else
        echo "‚ö†Ô∏è –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –ø–æ—Å–ª–µ–¥–Ω–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞"
    fi
else
    echo "‚ùå –§–∞–π–ª –ª–æ–≥–æ–≤ –Ω–µ –Ω–∞–π–¥–µ–Ω: $LOG_FILE"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç–∞—Ç—É—Å–∞ —Å–µ—Ä–≤–∏—Å–æ–≤
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç–∞—Ç—É—Å–∞ —Å–µ—Ä–≤–∏—Å–æ–≤:"
sudo systemctl is-active advertisements > /dev/null && echo "‚úÖ Django: –∞–∫—Ç–∏–≤–µ–Ω" || echo "‚ùå Django: –Ω–µ–∞–∫—Ç–∏–≤–µ–Ω"
sudo systemctl is-active nginx > /dev/null && echo "‚úÖ Nginx: –∞–∫—Ç–∏–≤–µ–Ω" || echo "‚ùå Nginx: –Ω–µ–∞–∫—Ç–∏–≤–µ–Ω"
sudo systemctl is-active postgresql > /dev/null && echo "‚úÖ PostgreSQL: –∞–∫—Ç–∏–≤–µ–Ω" || echo "‚ùå PostgreSQL: –Ω–µ–∞–∫—Ç–∏–≤–µ–Ω"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞
DISK_USAGE=$(df -h / | awk 'NR==2 {print $5}' | sed 's/%//')
if [ "$DISK_USAGE" -gt 80 ]; then
    echo "‚ö†Ô∏è –í–Ω–∏–º–∞–Ω–∏–µ: –¥–∏—Å–∫ –∑–∞–ø–æ–ª–Ω–µ–Ω –Ω–∞ ${DISK_USAGE}%"
fi
EOF

chmod +x "$PROJECT_DIR/monitor_updates.sh"

# –°–æ–∑–¥–∞–Ω–∏–µ –∞–ª–∏–∞—Å–∞ –¥–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
echo "alias monitor-ads='$PROJECT_DIR/monitor_updates.sh'" >> ~/.bashrc

log "‚úÖ –°–∫—Ä–∏–ø—Ç –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ —Å–æ–∑–¥–∞–Ω: monitor-ads"

# –°–æ–∑–¥–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏ –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ —Å—Ç–∞—Ä—ã—Ö –ª–æ–≥–æ–≤
(crontab -l 2>/dev/null; echo "0 1 * * 0 find /var/log/django -name '*.log' -mtime +30 -delete") | crontab -

log "‚úÖ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
log ""
log "üìã –î–æ—Å—Ç—É–ø–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã:"
log "   - monitor-ads: –ø—Ä–æ–≤–µ—Ä–∏—Ç—å —Å—Ç–∞—Ç—É—Å –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π"
if [ "$UPDATE_TYPE" = "manual" ]; then
    log "   - update-ads: –ø–æ–ª–Ω–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ"
    log "   - update-ads-quick: –±—ã—Å—Ç—Ä–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ"
    log "   - update-ads-safe: –±–µ–∑–æ–ø–∞—Å–Ω–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ"
fi
log ""
log "üìù –î–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤—ã–ø–æ–ª–Ω–∏—Ç–µ: source ~/.bashrc"
